#Square Root (`math.sqrt`):
#Example:
import math
number = 16
result = math.sqrt(number)
print("Square root of 16:", result)
# Power (`math.pow`):
# Example:
base = 2
exponent = 3
result = math.pow(base, exponent)
print("2 raised to the power 3:", result)
# Absolute Value (`abs`):
# Example:
number = -7
result = abs(number)
print("Absolute value of -7:", result)
# 2. Trigonometric Functions
# Sine (`math.sin`):
# Example:
import math
angle = math.radians(30)
result = math.sin(angle)
print("Sine of 30 degrees:", result)
# Cosine (`math.cos`):
# Example:
angle = math.radians(60)
result = math.cos(angle)
print("Cosine of 60 degrees:", result)
# Tangent (`math.tan`):
# Example:
angle = math.radians(45)
result = math.tan(angle)
print("Tangent of 45 degrees:", result)
# 3. Logarithmic Functions
# Natural Logarithm (`math.log`):
# Example:
number = 10
result = math.log(number)
print("Natural logarithm of 10:", result)
# Logarithm with Base 10 (`math.log10`):
# Example:
result = math.log10(100)
print("Logarithm base 10 of 100:", result)
